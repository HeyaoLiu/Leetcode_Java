package LC27RemoveElement;
//https://leetcode.com/articles/remove-element/
//?do not understand why the time complexity is lower?
public class OptimalTwoPointer {
    public int removeElement(int[] nums, int val) {
        int i = 0;
        int n = nums.length;
        while (i < n) {
            if (nums[i] == val) {
                nums[i] = nums[n - 1];
                // reduce array size by one
                n--;
            } else {
                i++;
            }
        }
        return n;
    }
}
